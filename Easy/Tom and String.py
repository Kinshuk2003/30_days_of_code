class Solution:
    def hashString(self, S):
        ht={'a':0,'b':1,'c':2,'d':3,'e':4,'f':5,'g':6,'h':7,'i':8,'j':9,'k':10,'l':11,'m':12,'n':13,
        'o':14,'p':15,'q':16,'r':17,'s':18,'t':19,'u':20,'v':21,'w':22,'x':23,'y':24,'z':25,'1':26,
        '2':27,'3':28,'4':29,'5':30,'6':31,'7':32,'8':33,'9':34,'0':35,'A':36,'B':37,'C':38,'D':39,
        'E':40,'F':41,'G':42,'H':43,'I':44,'J':45,'K':46,'L':47,'M':48,'N':49,'O':50,'P':51,'Q':52,
        'R':53,'S':54,'T':55,'U':56,'V':57,'W':58,'X':59,'Y':60,'Z':61}
        s=list(S.split())
        n=len(s)
        ss=0
        for i in range(n):
            x=0
            for j in s[i]:
                ss +=(ht[j])
            x=len(s[i])-1
            ss +=(x*(x+1)//2)
        
        return n*ss
